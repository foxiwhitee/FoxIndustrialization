package foxiwhitee.FoxIndustrialization.integration.cofh.client.gui;

import foxiwhitee.FoxIndustrialization.FICore;
import foxiwhitee.FoxIndustrialization.integration.cofh.container.ContainerPowerConverter;
import foxiwhitee.FoxIndustrialization.integration.cofh.network.packets.C2SSetPowerConverterMode;
import foxiwhitee.FoxIndustrialization.integration.cofh.tile.TilePowerConverter;
import foxiwhitee.FoxIndustrialization.integration.cofh.utils.ButtonConverterMode;
import foxiwhitee.FoxIndustrialization.tile.machines.TileBaseMachine;
import foxiwhitee.FoxLib.client.gui.FoxBaseGui;
import foxiwhitee.FoxLib.client.gui.buttons.NoTextureButton;
import foxiwhitee.FoxLib.container.FoxBaseContainer;
import foxiwhitee.FoxLib.network.NetworkManager;
import foxiwhitee.FoxLib.utils.ProductivityUtil;
import foxiwhitee.FoxLib.utils.helpers.EnergyUtility;
import foxiwhitee.FoxLib.utils.helpers.LocalizationUtils;
import foxiwhitee.FoxLib.utils.helpers.UtilGui;
import net.minecraft.client.gui.GuiButton;
import net.minecraft.inventory.Container;

public class GuiPowerConverter extends FoxBaseGui {
    private final TilePowerConverter tile;

    public GuiPowerConverter(ContainerPowerConverter container) {
        super(container, 262, 277);
        setModID(FICore.MODID);
        tile = (TilePowerConverter) container.getTileEntity();
    }

    @Override
    public void drawBG(int offsetX, int offsetY, int mouseX, int mouseY) {
        super.drawBG(offsetX, offsetY, mouseX, mouseY);

        this.bindTexture(FICore.MODID, "gui/names.png");
        int x = 72 - (89 / 2);
        UtilGui.drawTexture(offsetX + 55 + x, offsetY + 6, 0, 200, 89, 7, 89, 7, 512, 512);

        this.bindTexture(FICore.MODID, this.getBackground());
    }

    @Override
    public void drawFG(int offsetX, int offsetY, int mouseX, int mouseY) {
        this.bindTexture(FICore.MODID, this.getBackground());
        ButtonConverterMode mode = tile.getMode();
        UtilGui.drawTexture(106, 138, mode.getRfX(), mode.getRfY(), 14, 14, 14, 14, 512, 512);
        UtilGui.drawTexture(142, 138, mode.getEuX(), mode.getEuY(), 14, 14, 14, 14, 512, 512);
        double storedEnergy = tile.getEnergy();
        if (storedEnergy >= 0.0D) {
            double maxStoredEnergy = tile.getMaxEnergy();
            double y = ProductivityUtil.gauge(73, storedEnergy, maxStoredEnergy);
            UtilGui.drawTexture(141, 129 - y, 288, 0, 16, y, 16, y, 512.0D, 512.0D);
            drawIfInMouse(mouseX, mouseY, 139, 54, 20, 77,
                EnergyUtility.formatNumber(storedEnergy) + " / " + EnergyUtility.formatNumber(maxStoredEnergy) + " EU\n" +
                    LocalizationUtils.localize("tooltip.output", EnergyUtility.formatNumber(tile.getOutputEU()) + " EU/t"));
            this.bindTexture(FICore.MODID, this.getBackground());
        }
        storedEnergy = tile.getEnergyRF();
        if (storedEnergy >= 0.0D) {
            double maxStoredEnergy = tile.getMaxEnergyRF();
            double y = ProductivityUtil.gauge(73, storedEnergy, maxStoredEnergy);
            UtilGui.drawTexture(105, 129 - y, 272, 0, 16, y, 16, y, 512.0D, 512.0D);
            drawIfInMouse(mouseX, mouseY, 103, 54, 20, 77,
                EnergyUtility.formatNumber(storedEnergy) + " / " + EnergyUtility.formatNumber(maxStoredEnergy) + " RF\n" +
                    LocalizationUtils.localize("tooltip.output", EnergyUtility.formatNumber(tile.getOutputRF()) + " RF/t"));
            this.bindTexture(FICore.MODID, this.getBackground());
        }
    }

    @Override
    protected String getBackground() {
        return "gui/guiPowerConverter.png";
    }

    @Override
    public void initGui() {
        super.initGui();
        buttonList.add(new NoTextureButton(0, this.guiLeft + 142, this.guiTop + 138, 14, 14, "tooltip.convert.eu"));
        buttonList.add(new NoTextureButton(1, this.guiLeft + 106, this.guiTop + 138, 14, 14, "tooltip.convert.rf"));
    }

    @Override
    protected void actionPerformed(GuiButton button) {
        super.actionPerformed(button);
        if (button instanceof NoTextureButton) {
            if (button.id == 0) {
                NetworkManager.instance.sendToServer(new C2SSetPowerConverterMode(tile.xCoord, tile.yCoord, tile.zCoord, ButtonConverterMode.EU));
            } else if (button.id == 1) {
                NetworkManager.instance.sendToServer(new C2SSetPowerConverterMode(tile.xCoord, tile.yCoord, tile.zCoord, ButtonConverterMode.RF));
            }
        }
    }
}
